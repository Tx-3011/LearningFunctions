import java.util.Scanner;


public class Main {
    public static void main(String[] args) {

        Scanner input = new Scanner(System.in);
        int a = input.nextInt();
        int r = a % 2;
        if (r == 0) {
            System.out.println("The number is even");
        }else {
            System.out.println("The number is odd");
        }

















    }

}